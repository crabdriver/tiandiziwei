/// 星曜亮度等级
enum Brightness: String, CaseIterable {
    case miao = "庙"    // 最旺
    case wang = "旺"
    case de = "得"
    case li = "利"
    case ping = "平"
    case bu = "不"
    case xian = "陷"    // 最弱
    
    /// 数值表示（用于分析比较）
    var value: Int {
        switch self {
        case .miao: return 7
        case .wang: return 6
        case .de: return 5
        case .li: return 4
        case .ping: return 3
        case .bu: return 2
        case .xian: return 1
        }
    }
}

